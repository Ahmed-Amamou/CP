const double EPS = 0.00000001;
const ll MOD = 1e9 + 7;
int gcd(ll a, ll b) { return b ? gcd(b, a % b) : a; }
int lcm(int a, int b) { return a * (b / gcd(a, b)); }

void solve()
{
    ll n;
    cin >> n;

    map<ll, ll> numbers;
    ll res = 0;
    ll z = (1LL << 31) - 1;
    cout << "z: " << bitset<32>(z) << endl;
    for (int i = 0; i < n; i++)
    {
        ll x, y;
        cin >> x;
        y = z ^ x;
        cout << "y: "<<bitset<32>(y)<<endl;
        if (numbers[x] != 0)
        {
            numbers[x]--;
        }
        else
        {
            res++;
            numbers[y]++;
        }
    }
    cout << res << endl;
}

