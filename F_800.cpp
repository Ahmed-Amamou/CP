void solve()
{
    ll n, m;
    cin >> n >> m;
    char c;
    cin >> c;
    ll c_counter = 0;
    ll c_counter_last_row = 0;
    for (int i = 0; i < n; i++)
    {
        string s;
        cin >> s;
        for (int j = 0; j < m; j++)
        {
            if (i != n - 1)
            {
                if (s[j] == c)
                    c_counter++;
            }
            else
            {
                if (s[j] == c)
                {
                    c_counter++;
                    c_counter_last_row++;
                }
            }
        }
    }
    // cout << c_counter << endl;
    if (m == 1 && c_counter != n)
    {
        cout << "NO" << endl;
        return;
    }
    if (c_counter_last_row == m)
    {
        cout << "YES" << endl;
        return;
    }
    else
    {
        cout << "NO" << endl;
    }
}

