const double EPS = 0.00000001;
const ll MOD = 1e9 + 7;
int gcd(ll a, ll b) { return b ? gcd(b, a % b) : a; }
int lcm(int a, int b) { return a * (b / gcd(a, b)); }

void solve()
{
    ll x, y, z, f;
    cin >> x >> y >> z >> f;

    ll res = x;
    if (x == 0)
    {
        cout << 1 << endl;
        return;
    }
    else
    {
        res += 2 * min(y, z);

        // cout << res << endl;
        res += min(x + 1, abs(y - z) + f);
    }
    cout << res << endl;
}

