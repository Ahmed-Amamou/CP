int gcd(ll a, ll b) { return b ? gcd(b, a % b) : a; }
int lcm(int a, int b) { return a * (b / gcd(a, b)); }
ll bs(vector<ll> &v, ll x)
{
    ll res = -1;
    ll mid;
    ll low = 0, high = v.size() - 1;
    while (low <= high)
    {
        mid = low + (high - low) / 2;
        if (v[mid] <= x)
        {
            res = mid;
            low = mid + 1;
        }
        else
        {
            high = mid - 1;
        }
    }
    return res;
}
