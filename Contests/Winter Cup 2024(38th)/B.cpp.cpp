const double EPS = 0.00000001;
const ll MOD = 1e9 + 7;
int gcd(ll a, ll b) { return b ? gcd(b, a % b) : a; }
int lcm(int a, int b) { return a * (b / gcd(a, b)); }

void solve()
{
    ll n, k;
    cin >> n >> k;
    vector<ll> a(n);
    for (int i = 0; i < n; i++)
    {
        cin >> a[i];
    }
    if(n==1){
        cout << 0 <<endl;
        return;
    }
    sort(a.begin(), a.end());
    ll i = 1, M = min(abs(a[0] - a[n - 1]), abs(a[n - 1] - k - (a[0] + k)));
    while (M < a[i] - a[n - 1-i])
    {
        M = min({abs(a[i] - a[n - 1 - i]), abs(a[n - 1 - i] - k - (a[i] + k)),M});
        i++;
    }
    cout << M << endl;
}

signed main()
{
    FAST;
    ll tt = 1;
    // freopen("input.in", "r", stdin);
    cin >> tt;
    while (tt--)
        solve();
    return 0;
}