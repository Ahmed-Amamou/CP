void solve()
{
    ll n;
    cin >> n;
    string s;
    cin >> s;
    ll count = 0;
    for (int i = 0; i + 2 < n; i++)
    {
        if ((s[i] == 'm' && s[i + 1] == 'a' && s[i + 2] == 'p'))
        {
            count += 1;
            s[i + 2] = 'x';
        }
        if( (s[i] == 'p' && s[i + 1] == 'i' && s[i + 2] == 'e')){
            count += 1;
            s[i] = 'x';
        }
    }
    cout << count << endl;
}

