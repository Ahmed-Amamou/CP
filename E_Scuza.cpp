int gcd(ll a, ll b) { return b ? gcd(b, a % b) : a; }
int lcm(int a, int b) { return a * (b / gcd(a, b)); }

ll binary_search(ll x , vector<ll>& v){
    ll high = v.size() - 1, low = 0;
    ll result = -1 ;
    while(low<=high){
        ll mid = low + (high - low)/2 ;
        if(v[mid] <= x) {
            result = mid;
            low = mid + 1;
        }
        else high = mid - 1;

    }
    return result;
}

