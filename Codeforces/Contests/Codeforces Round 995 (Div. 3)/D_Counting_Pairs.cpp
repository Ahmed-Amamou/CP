const double EPS = 0.00000001;
const ll MOD = 1e9 + 7;
int gcd(ll a, ll b) { return b ? gcd(b, a % b) : a; }
int lcm(int a, int b) { return a * (b / gcd(a, b)); }

void solve()
{
    int n;
    ll x, y;
    cin >> n >> x >> y;
    vector<ll> a(n);
    for (int i = 0; i < n; i++)
    {
        cin >> a[i];
    }
    ll sum = 0;
    for (int i = 0; i < n; i++)
    {
        sum += a[i];
    }
    sort(a.begin(), a.end());
    ll low = sum - y;
    ll high = sum - x;
    ll cnt = 0;
    for (int i = 0; i < n; i++)
    {
        auto l = lower_bound(a.begin() + i + 1, a.end(), low - a[i]);
        auto r = upper_bound(a.begin() + i + 1, a.end(), high - a[i]);

        cnt += (r - l);
    }

    cout << cnt << endl;
}

signed main()
{
    FAST;
    ll tt = 1;
    // freopen("input.in", "r", stdin);
    cin >> tt;
    while (tt--)
        solve();
    return 0;
}