const double EPS = 0.00000001;
const ll MOD = 1e9 + 7;
int gcd(ll a, ll b) { return b ? gcd(b, a % b) : a; }
int lcm(int a, int b) { return a * (b / gcd(a, b)); }

void solve()
{
    int n;
    ll k;
    cin >> n >> k;

    vector<ll> a(n), b(n);
    for (int i = 0; i < n; i++)
        cin >> a[i];
    for (int i = 0; i < n; i++)
        cin >> b[i];

    sort(a.begin(), a.end());
    sort(b.begin(), b.end());

    vector<ll> p(2 * n);
    for (auto x : a)
        p.push_back(x);
    for (auto x : b)
        p.push_back(x);

    sort(p.begin(), p.end());
    p.erase(unique(p.begin(), p.end()), p.end());

    ll mx = 0;

    for (auto p_val : p)
    {
        int lowA = lower_bound(a.begin(), a.end(), p_val) - a.begin();
        int lowB = lower_bound(b.begin(), b.end(), p_val) - b.begin();

        ll SoFAR = (ll)lowA - (ll)lowB;

        if (SoFAR <= k)
        {

            ll earnings = p_val * (n - lowB);
            mx = max(mx, earnings);
        }
    }

    cout << mx << endl;
}

signed main()
{
    FAST;
    ll tt = 1;
    // freopen("input.in", "r", stdin);
    cin >> tt;
    while (tt--)
        solve();
    return 0;
}