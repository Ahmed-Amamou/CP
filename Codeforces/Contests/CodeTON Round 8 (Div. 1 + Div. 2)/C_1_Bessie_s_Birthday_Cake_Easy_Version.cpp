int gcd(ll a, ll b) { return b ? gcd(b, a % b) : a; }
int lcm(int a, int b) { return a * (b / gcd(a, b)); }

void solve()
{
    ll n, x, y;
    cin >> n >> x >> y;
    ll a[x];
    for (int i = 0; i < x; i++)
    {
        cin >> a[i];
    }
    sort(a, a + x);
    ll ans = 0LL;
    ans += x - 2;

    for (int i = 0; i <= x - 2; i++)
    {
        ans += ((a[i] + 2) == a[i + 1] ? 1 : 0);
    }

    if (((a[x - 1] + 2) % n == a[0]) && x >= 2)
    {
        ans++;
    }
    cout << ans << endl;
}

