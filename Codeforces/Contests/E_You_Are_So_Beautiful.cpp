const double EPS = 0.00000001;
const ll MOD = 1e9 + 7;
int gcd(ll a, ll b) { return b ? gcd(b, a % b) : a; }
int lcm(int a, int b) { return a * (b / gcd(a, b)); }


int countSubarrays(vector<int> &arr)
{
    int n = arr.size();
    unordered_map<int, int> rightmost;
    vector<int> suffixSum(n + 1, 0);
    int count = 0;

    for (int i = 0; i < n; i++)
    {
        suffixSum[i + 1] = suffixSum[i] + arr[i];
        rightmost[arr[i]] = i;
    }

    for (int i = 0; i < n; i++)
    {
        int left = i + 1;
        int right = rightmost[arr[i]] + 1;
        count += (n - right + 1) * (suffixSum[left] - suffixSum[i]);
    }

    return count;
}


