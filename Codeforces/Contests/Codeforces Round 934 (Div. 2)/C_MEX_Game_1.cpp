const double EPS = 0.00000001;
const ll MOD = 1e9 + 7;
int gcd(ll a, ll b) { return b ? gcd(b, a % b) : a; }
int lcm(int a, int b) { return a * (b / gcd(a, b)); }

void solve()
{
    ll n;
    cin >> n;
    ll a[n];
    vector<int> count(n + 1, 0);
    for (int i = 0; i < n; i++)
    {
        cin >> a[i];
        count[a[i]]++;
    }
    bool flag = true;
    int res = 0;
    for (int it = 0; it <= n; it++)
    {
        if (count[it] == 0)
        {
            res = it;
            break;
        }
        else if (count[it] == 1)
        {
            if (flag)
            {
                flag = false;
            }
            else
            {
                res = it;
                break;
            }
        }
    }

    cout << res << endl;
}

signed main()
{
    FAST;
    ll tt = 1;
    // freopen("input.in", "r", stdin);
    cin >> tt;
    while (tt--)
        solve();
    return 0;
}