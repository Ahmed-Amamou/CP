const double EPS = 0.00000001;
const ll MOD = 1e9 + 7;
int gcd(ll a, ll b) { return b ? gcd(b, a % b) : a; }
int lcm(int a, int b) { return a * (b / gcd(a, b)); }

void solve()
{
    int n, q, m;
    cin >> n >> q >> m;
    int d1, d2;
    cin >> d1 >> d2;
    int pos;
    cin >> pos;
    if (pos <= min(d1, d2))
    {
        cout << min(d1, d2) - 1 << endl;
        return;
    }
    else if (pos >= max(d1, d2))
    {
        cout << n - max(d1, d2) << endl;
    }
    else
    {
        cout << (max(d2, d1) - min(d1, d2)) / 2 << endl;
    }
}

signed main()
{
    FAST;
    ll tt = 1;
    // freopen("input.in", "r", stdin);
    cin >> tt;
    while (tt--)
        solve();
    return 0;
}