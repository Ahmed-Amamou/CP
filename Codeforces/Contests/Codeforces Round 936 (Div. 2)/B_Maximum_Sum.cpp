using namespace std;

const double EPS = 0.00000001;
const ll MOD = 1e9 + 7;
int gcd(ll a, ll b) { return b ? gcd(b, a % b) : a; }
int lcm(int a, int b) { return a * (b / gcd(a, b)); }

void solve()
{
    ll n, k;
    cin >> n >> k;
    ll a[n];
    for (int i = 0; i < n; i++)
    {
        cin >> a[i];
    }
    ll best = 0, sum = 0;
    for (int i = 0; i < n; i++)
    {
        sum = max(a[i], sum + a[i]);
        best = max(best, sum);
    }
    ll MSS = (best + MOD) % MOD;
    ll total = 0;
    for (int i = 0; i < k; i++)
    {
        total = (total + MSS) % MOD;
        MSS = (MSS * 2) % MOD;
    }

    for (int i = 0; i < n; i++)
    {
        total = (total + a[i]) % MOD;
    }
    cout << (total+MOD)%MOD << endl;
}

signed main()
{
    FAST;
    ll tt = 1;
    // freopen("input.in", "r", stdin);
    cin >> tt;
    while (tt--)
        solve();
    return 0;
}