// construct a set that will contain all the lines that contain stormtroopers
// check every time if storm trooper belongs to one of the lines of the set if not we add "pente" & "b": coordonnées à l'origine
// then we print the length of the set
void solve()
{
    set<pair<double, double>> lines;
    ll n, x, y;
    cin >> n >> x >> y;
    int on_vertical = 0;
    for (int i = 0; i < n; i++)
    {

        ll x1, y1;
        double m, b;

        cin >> x1 >> y1;
        if (x1 == x)
        {
            on_vertical = 1;
            continue;
        }
        m = (double)(y1 - y) / (double)(x1 - x);
        b = y - m * x;
        // cout << m << " " << b << endl;
        lines.insert(make_pair(m, b));
    }
    // for (auto it = lines.begin(); it != lines.end(); ++it) {
    //     cout << it->first << " " << it->second << endl;
    // }
    cout << lines.size() + on_vertical << endl;
}

