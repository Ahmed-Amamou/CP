void solve()
{

    int n;
    cin >> n;
    vector<int> arr(n);
    for (int i = 0; i < n; ++i)
    {
        cin >> arr[i];
    }

    bool foundIncreasing = false;

    for (int shift = 0; shift < n; ++shift)
    {
        bool isIncreasing = true;
        for (int i = 1; i < n; ++i)
        {
            if (arr[i] < arr[i - 1])
            {
                isIncreasing = false;
                break;
            }
        }
        if (isIncreasing)
        {
            foundIncreasing = true;
            break;
        }

        int first = arr[0];
        for (int i = 0; i < n - 1; ++i)
        {
            arr[i] = arr[i + 1];
        }
        arr[n - 1] = first;
        // for (int i = 0; i < n; ++i)
        // {
        //     cout << arr[i] << " ";
        // }
        // cout << endl;
    }

    if (foundIncreasing)
    {
        cout << "Yes" << endl;
    }
    else
    {
        cout << "No" << endl;
    }
}

