const double EPS = 0.00000001;
const ll MOD = 1e9+7;
int gcd (ll a, ll b) { return b ? gcd (b, a % b) : a;}
int lcm (int a, int b) { return a * (b / gcd(a, b));}
const int N= 1e5+5;
int tree[N+N],n;
pair<int,int> h[N];

int get(int l,int r){
	l= l+n-1,r+=n;
	int res=0;
	while(l<r){
		if(l&1)res= gcd(res,tree[l++]);
		if(r&1)res= gcd(res,tree[--r]);
		l>>=1,r>>=1;
	}
	return res;
} 

