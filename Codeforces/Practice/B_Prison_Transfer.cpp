int gcd(ll a, ll b) { return b ? gcd(b, a % b) : a; }
int lcm(int a, int b) { return a * (b / gcd(a, b)); }

void solve()
{
    ll n, t, c;
    cin >> n >> t >> c;
    ll a[n];
    for (ll i = 0; i < n; i++)
    {
        cin >> a[i];
    }
    vector<pair<int, int>> v;
    for (int i = 0; i < n; i++)
    {
        if (a[i] <= t)
        {
            pair<int, int> p = make_pair(i, i);
            while (true)
            {
                i++;
                if (i >= n || a[i] > t)
                    break;
                p.second = i;
            }
            v.push_back(p);
        }
    }
    ll cnt = 0;
    for (auto p : v)
    {
        cnt += max((p.second - p.first - c + 2), 0LL);
    }
    cout << cnt << endl;
}

