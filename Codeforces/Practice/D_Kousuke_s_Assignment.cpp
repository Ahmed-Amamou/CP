int gcd(ll a, ll b) { return b ? gcd(b, a % b) : a; }
int lcm(int a, int b) { return a * (b / gcd(a, b)); }

void solve()
{
    int n;
    cin >> n;

    vector<int> a(n); 
    for (int i = 0; i < n; i++)
    {
        cin >> a[i];
    }

    ll prefix_sum = 0;
    set<ll> seen;
    seen.insert(0); 
    ll count_segments = 0;
    ll last_index = -1;

    for (ll i = 0; i < n; i++)
    {
        prefix_sum += a[i];
        if (seen.count(prefix_sum))
        {
            count_segments++;
            seen.clear();
            seen.insert(0);
            prefix_sum = 0;
        }
        seen.insert(prefix_sum);
    }
    cout << count_segments << endl;
}

