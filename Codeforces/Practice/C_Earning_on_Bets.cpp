using namespace std;

const double EPS = 0.00000001;
const ll MOD = 1e9 + 7;
int gcd(ll a, ll b) { return b ? gcd(b, a % b) : a; }
int lcm(int a, int b) { return a * (b / gcd(a, b)); }

void solve()
{
    int n;
    cin >> n;
    int a[n];
    int w = 1;
    for (int i = 0; i < n; i++)
    {
        cin >> a[i];
        w *= a[i] / gcd(w, a[i]);
    }
    ll s = 0;
    ll res[n];
    for (int i = 0; i < n; i++)
    {
        int local = w / a[i];
        s += local;
        res[i] = local;
    }

    if (w > s)
    {
        for (int i = 0; i < n; i++)
        {
            cout << res[i] << " ";
        }
        cout << endl;
    }
    else
    {
        cout << -1 << endl;
    }
}

signed main()
{
    FAST;
    ll tt = 1;
    // freopen("input.in", "r", stdin);
    cin >> tt;
    while (tt--)
        solve();
    return 0;
}