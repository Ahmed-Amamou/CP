int gcd(ll a, ll b) { return b ? gcd(b, a % b) : a; }
int lcm(int a, int b) { return a * (b / gcd(a, b)); }

void solve()
{
    // Check last d (d_m) and see if it exists in the found array b1..bn
    // if it's there we go on and check if the changed numbers exist in d or not

    int n;
    cin >> n;
    vector<int> a(n), b(n);
    for (int i = 0; i < n; i++)
    {
        cin >> a[i];
    }
    for (int i = 0; i < n; i++)
    {
        cin >> b[i];
    }
    map<int, int> modified_nums;
    for (int i = 0; i < n; i++)
    {
        if (b[i] != a[i])
        {
            modified_nums[b[i]]++;
        }
    }

    int m;
    cin >> m;
    vector<int> modif(m);
    for (int i = 0; i < m; i++)
    {
        cin >> modif[i];
    }
    if (find(b.begin(), b.end(), modif[m - 1]) != b.end())
    {
        // cout << "last element is here!" << endl;
        for (int i = 0; i < m; i++)
        {
            if (modified_nums.find(modif[i]) != modified_nums.end())
            {
                modified_nums[modif[i]]--;
                if (modified_nums[modif[i]] == 0)
                {
                    modified_nums.erase(modif[i]);
                }
            }
        }
        if (modified_nums.empty())
        {
            cout << "YES" << endl;
        }
        else
        {
            cout << "NO" << endl;
        }
    }
    else
    {
        cout << "NO" << endl;
    }
}

signed main()
{
    FAST;
    ll tt = 1;
    // freopen("input.in", "r", stdin);
    cin >> tt;
    while (tt--)
        solve();
    return 0;
}