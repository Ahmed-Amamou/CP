const double EPS = 0.00000001;
const ll MOD = 1e9 + 7;
int gcd(ll a, ll b) { return b ? gcd(b, a % b) : a; }
int lcm(int a, int b) { return a * (b / gcd(a, b)); }

void solve()
{
    int x, y, k;
    cin >> x >> y >> k;
    ll first = ceil((x) * 1.0 / k);
    ll second = ceil((y) * 1.0 / k);
    if (first > second)
        cout << first * 2 - 1 << endl;
    else
        cout << second * 2 << endl;
}

