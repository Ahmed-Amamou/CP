const double EPS = 0.00000001;
const ll MOD = 1e9 + 7;
int gcd(ll a, ll b) { return b ? gcd(b, a % b) : a; }
int lcm(int a, int b) { return a * (b / gcd(a, b)); }

void solve()
{
    ll n, m;
    cin >> n >> m;
    map<char, int> mp;
    string s;
    cin >> s;
    for (char c = 'A'; c <= 'G'; c++)
    {
        mp[c] = 0;
    }
    for (int i = 0; i < n; i++)
    {
        mp[s[i]]++;
    }
    ll count = 0;
    for (auto it : mp)
    {
        if (it.second < m)
        {
            count += m - it.second;
        }
    }
    cout << count << endl;
}

signed main()
{
    FAST;
    ll tt = 1;
    // freopen("input.in", "r", stdin);
    cin >> tt;
    while (tt--)
        solve();
    return 0;
}