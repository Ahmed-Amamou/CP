const double EPS = 0.00000001;
const ll MOD = 1e9 + 7;
int gcd(ll a, ll b) { return b ? gcd(b, a % b) : a; }
int lcm(int a, int b) { return a * (b / gcd(a, b)); }

void solve()
{
    int n, k;
    cin >> n >> k;
    int a[n];
    for (int i = 0; i < n; i++)
    {
        cin >> a[i];
    }
    int last;
    for (int i = 0; i < k; i++)
    {
        int j;
        for (j = 0; j < n - 1; j++)
        {
            if (a[j] < a[j + 1])
            {
                last = j + 1;
                a[j]++;
                break;
            }
        }
        
        if (j + 1 == n)
        {
            cout << -1 << endl;
            return;
        }
    }
    cout << last << endl;
}

signed main()
{
    FAST;
    ll tt = 1;
    // freopen("input.in", "r", stdin);
    cin >> tt;
    while (tt--)
        solve();
    return 0;
}