const double EPS = 0.00000001;
const ll MOD = 1e9 + 7;
int gcd(ll a, ll b) { return b ? gcd(b, a % b) : a; }
int lcm(int a, int b) { return a * (b / gcd(a, b)); }

void solve()
{
    int n;
    cin >> n;
    vector<int> a(n);
    for (int i = 0; i < n; i++)
    {
        cin >> a[i];
    }
    sort(a.begin(), a.end());
    // go through the elements of the array from 0 to <n-2
    ll mn = INT32_MAX;
    auto good = [&](ll i, ll mid) -> bool
    {
        return (a[i] + a[i + 1]) <= a[mid];
    };
    for (int i = 0; i < n - 2; i++)
    {
        // find the closest to left of a[i] + a[i+1]
        ll l = i - 1;
        ll r = n;
        while (r - l > 1)
        {
            ll mid = (l + r) / 2;
            // debug(l);
            // debug(r);
            // debug(mid);
            if (good(i, mid))
                r = mid;
            else
                l = mid;
        }
        mn = min(mn, i + n - l - 1);
    }
    cout << mn << endl;

}

signed main()
{
    FAST;
    ll tt = 1;
    // freopen("input.in", "r", stdin);
    cin >> tt;
    while (tt--)
        solve();
    return 0;
}