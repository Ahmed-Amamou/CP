using namespace std;
#define debug(x) cout << #x << ": " << x << endl;

const double EPS = 0.00000001;
const ll MOD = 1e9 + 7;
int gcd(ll a, ll b) { return b ? gcd(b, a % b) : a; }
int lcm(int a, int b) { return a * (b / gcd(a, b)); }

void solve()
{
    int n;
    cin >> n;
    vector<vector<ll>> matrix(n, vector<ll>(n));
    for (int i = 0; i < n; i++)
    {
        for (int j = 0; j < n; j++)
        {
            cin >> matrix[i][j];
        }
    }
    ll sum = 0;
    for (int start_col = n - 1; start_col >= 0; start_col--)
    {
        int i = 0, j = start_col;
        int mn = 0LL;
        while (i < n && j < n)
        {
            if (mn > matrix[i][j])
                mn = matrix[i][j];
            i++;
            j++;
        }
        sum += abs(mn);
    }

    // Then start from the second row of the first column (to avoid repeating the first diagonal)
    for (int start_row = 1; start_row < n; start_row++)
    {
        int i = start_row, j = 0;
        int mn = 0LL;

        while (i < n && j < n)
        {
            if (mn > matrix[i][j])
                mn = matrix[i][j];
            i++;
            j++;
        }
        sum += abs(mn);
    }
    cout << sum << endl;
}

signed main()
{
    FAST;
    ll tt = 1;
    // freopen("input.in", "r", stdin);
    cin >> tt;
    while (tt--)
        solve();
    return 0;
}