const double EPS = 0.00000001;
const ll MOD = 1e9 + 7;
int gcd(ll a, ll b) { return b ? gcd(b, a % b) : a; }
int lcm(int a, int b) { return a * (b / gcd(a, b)); }
double cubicroot(int x)
{
    return pow(x, 1.0 / 3.0);
}

void solve()
{
    ll l, h, w, d;
    cin >> l >> w >> h >> d;
    ll best = 0;
    for (int li = 1; li <= l; li++)
    {
        for (int wi = 1; wi <= w; wi++)
        {
            ll hi = d / (li * wi);
            if (hi <= h && d % (li * wi) == 0)
            {
                best = max(best, (l - li + 1) * (w - wi + 1) * (h - hi + 1));
            }
        }
    }
    cout << best << endl;
}

signed main()
{
    FAST;
    ll tt = 1;
    // freopen("input.in", "r", stdin);
    cin >> tt;
    while (tt--)
        solve();
    return 0;
}