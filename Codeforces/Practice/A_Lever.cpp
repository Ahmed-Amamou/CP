int lcm(int a, int b) { return a * (b / gcd(a, b)); }

void solve()
{
    string s;
    cin >> s;
    ll pivot;
    for (int i = 0; i < s.size(); i++)
    {
        if (s[i] == '^')
        {
            pivot = i;
            break;
        }
    }
    ll left = 0, right = 0;
    for (int i = 0; i < pivot; i++)
    {
        left += (isdigit(s[i]) ? (s[i] - '0') : 0) * (pivot - i);
    }

    for (int i = pivot + 1; i < s.size(); i++)
    {
        right += (isdigit(s[i]) ? (s[i] - '0') : 0) * (i - pivot);
    }
    if (right == left)
    {
        cout << "balance" << endl;
    }
    else if (right > left)
    {
        cout << "right" << endl;
    }
    else
    {
        cout << "left";
    }
}

