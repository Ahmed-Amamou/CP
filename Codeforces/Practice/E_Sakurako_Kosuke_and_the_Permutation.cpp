const double EPS = 0.00000001;
const ll MOD = 1e9 + 7;
int gcd(ll a, ll b) { return b ? gcd(b, a % b) : a; }
int lcm(int a, int b) { return a * (b / gcd(a, b)); }

void solve()
{
    int n;
    cin >> n;
    int a[n];
    for (int i = 0; i < n; i++)
    {
        cin >> a[i];
    }
    bool vis[n];
    ll count = 0LL;
    memset(vis, false, sizeof(vis));
    //detect cycles then 
    for (int i = 0; i < n; i++)
    {
        if (!vis[i])
        {
            int next = i;
            ll len = 0LL;
            while (!vis[next])
            {
                vis[next] = true;
                // update
                next = a[next] - 1;
                ++len;
            }
            count += (len - 1) / 2;
        }
    }
    cout << count << endl;
}

signed main()
{
    FAST;
    ll tt = 1;
    // freopen("input.in", "r", stdin);
    cin >> tt;
    while (tt--)
        solve();
    return 0;
}