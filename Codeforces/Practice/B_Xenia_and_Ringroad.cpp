void solve()
{
    ll n, k;
    cin >> n >> k;
    ll a[k];
    ll ans = 0;
    for (int i = 0; i < k; i++)
    {
        cin >> a[i];
    }
    if (a[0] != 1)
    {
        ans += a[0]-1;
    }
    for (int i = 1; i < k; i++)
    {
        if (a[i] >= a[i - 1])
        {
            ans += a[i] - a[i - 1];
        }
        else
        {
            ans += n - a[i - 1] + a[i];
        }
    }
    cout << ans << endl;
}

