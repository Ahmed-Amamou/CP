void solve()
{
    string s;
    cin >> s;
    string new_s = "";
    for (auto c : s)
    {
        if (c == 'q')
            new_s += 'p';
        else if (c == 'p')
            new_s += 'q';
        else
            new_s += c;
    }
    reverse(new_s.begin(), new_s.end());
    cout << new_s << endl;
}

