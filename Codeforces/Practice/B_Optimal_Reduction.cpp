const double EPS = 0.00000001;
const ll MOD = 1e9 + 7;
int gcd(ll a, ll b) { return b ? gcd(b, a % b) : a; }
int lcm(int a, int b) { return a * (b / gcd(a, b)); }

void solve()
{
    ll n;
    cin >> n;
    int a[n];
    for (int i = 0; i < n; i++)
    {
        cin >> a[i];
    }
    if (n <= 2)
    {
        cout << "YES" << endl;
    }
    else
    {
        bool flag1 = false, flag2 = false;
        for (int i = 1; i < n - 1; i++)
        {
            if (a[i] < a[i - 1])
            {
                flag1 = true;
            }
            if (a[i] < a[i + 1] && flag1 == true)
            {
                flag2 = true;
            }
        }
        if (flag1 && flag2)
        {
            cout << "NO" << endl;
        }
        else
        {
            cout << "YES" << endl;
        }
    }
}

