const double EPS = 0.00000001;
const ll MOD = 1e9 + 7;
int gcd(ll a, ll b) { return b ? gcd(b, a % b) : a; }
int lcm(int a, int b) { return a * (b / gcd(a, b)); }

void solve()
{
    ll n, k;
    cin >> n >> k;
    ll a[n];
    for (int i = 0; i < n; i++)
    {
        cin >> a[i];
    }
    // search for the longest sequence of elements that diffs don't exceed k;
    // if (n == 1)
    // {
    //     cout << 0 << endl;
    //     return;
    // }
    sort(a, a + n);
    ll mx = 0;
    ll tmp = 0;
    for (int i = 0; i < n - 1; i++)
    {
        if (a[i + 1] - a[i] > k)
        {
            tmp = 0;
            continue;
        }
        tmp++;
        mx = max(tmp, mx);
    }
    cout << n - mx - 1 << endl;
}

signed main()
{
    FAST;
    ll tt = 1;
    // freopen("input.in", "r", stdin);
    cin >> tt;
    while (tt--)
        solve();
    return 0;
}