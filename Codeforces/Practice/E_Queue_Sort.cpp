int lcm(int a, int b) { return a * (b / gcd(a, b)); }

void solve()
{
    int n;
    cin >> n;
    vector<int> a(n);
    set<int> vis;
    int mn = INT32_MAX;
    int pos = 0;
    for (int i = 0; i < n; i++)
    {
        cin >> a[i];
        if (a[i] < mn)
        {
            mn = a[i];
            pos = i;
        }
    }
    bool flag = true;
    for (int i = pos + 1; i < n; i++)
    {
        if (a[i] < a[i - 1])
        {
            flag = false;
            break;
        }
    }
    if (flag)
    {
        cout << pos << endl;
    }
    else
    {
        cout << -1 << endl;
    }
}

