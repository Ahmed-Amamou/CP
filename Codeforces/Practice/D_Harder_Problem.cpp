using namespace std;
#define debug(x) cout << #x << ": " << x << endl;

const double EPS = 0.00000001;
const ll MOD = 1e9 + 7;
int gcd(ll a, ll b) { return b ? gcd(b, a % b) : a; }
int lcm(int a, int b) { return a * (b / gcd(a, b)); }

void solve()
{
    int n;
    cin >> n;
    int a[n], b[n];
    set<int> sett, unsett;
    for (int i = 1; i <= n; i++)
    {
        unsett.insert(i);
    }
    for (int i = 0; i < n; i++)
    {
        cin >> a[i];
        if (sett.count(a[i]))
        {
            b[i] = *unsett.begin();
            sett.insert(b[i]);
            unsett.erase(unsett.begin());
        }
        else
        {
            b[i] = a[i];
            sett.insert(a[i]);
            unsett.erase(a[i]);
        }
    }
    for (int i = 0; i < n; i++)
    {
        cout << b[i] << " ";
    }
    cout << endl;
}

signed main()
{
    FAST;
    ll tt = 1;
    // freopen("input.in", "r", stdin);
    cin >> tt;
    while (tt--)
        solve();
    return 0;
}
