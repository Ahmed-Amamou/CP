const double EPS = 0.00000001;
const ll MOD = 1e9 + 7;
int gcd(ll a, ll b) { return b ? gcd(b, a % b) : a; }
int lcm(int a, int b) { return a * (b / gcd(a, b)); }

void solve()
{
    int n, m, a, b;
    cin >> n >> m >> a >> b;
    if (n * a != m * b)
    {
        cout << "NO" << endl;
        return;
    }
    int d = 1;
    while ((d * n) % m != 0)
    {
        d++;
    }
    int grid[n][m];
    int beg = 0;
    memset(grid, 0, sizeof(grid));
    for (int i = 0; i < n; i++)
    {
        for (int j = beg; j < beg + a; j++)
        {
            grid[i][j % m]++;
        }
        beg += d;
        beg %= m;
    }
    cout << "YES" << endl;
    for (int i = 0; i < n; i++)
    {
        for (int j = 0; j < m; j++)
        {
            cout << grid[i][j];
        }
        cout << endl;
    }
}

signed main()
{
    FAST;
    ll tt = 1;
    // freopen("input.in", "r", stdin);
    cin >> tt;
    while (tt--)
        solve();
    return 0;
}