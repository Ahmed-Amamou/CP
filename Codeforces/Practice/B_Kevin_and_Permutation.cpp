const double EPS = 0.00000001;
const ll MOD = 1e9 + 7;
int gcd(ll a, ll b) { return b ? gcd(b, a % b) : a; }
int lcm(int a, int b) { return a * (b / gcd(a, b)); }

void solve()
{
    int n, k;
    cin >> n >> k;
    vector<int> p(n, 0);
    set<int> nums;
    for (int i = 1; i <= n; i++)
    {
        nums.insert(i);
    }
    for (int i = k - 1; i < n; i += k)
    {
        p[i] = *nums.begin();
        nums.erase(nums.begin());
    }
    for (int i = 0; i < n; i++)
    {
        if (p[i] == 0)
        {
            p[i] = *nums.begin();
            nums.erase(nums.begin());
        }
    }

    for (auto c : p)
    {
        cout << c << ' ';
    }
    cout << endl;
}

signed main()
{
    FAST;
    ll tt = 1;
    // freopen("input.in", "r", stdin);
    cin >> tt;
    while (tt--)
        solve();
    return 0;
}