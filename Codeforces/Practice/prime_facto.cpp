void solve()
{
    int n;
    cin >> n;
    vector<int> primefacto;
    for (int i = 2; i < (int)ceil(sqrt(n)) + 1; i++)
    {
        while (n % i == 0)
        {
            n /= i;
            primefacto.push_back(i);
        }
    }
    if (n != 1)
        primefacto.push_back(n);
    for (auto e : primefacto)
        cout << e << " ";
    cout << endl;
}

