int gcd(ll a, ll b) { return b ? gcd(b, a % b) : a; }
int lcm(int a, int b) { return a * (b / gcd(a, b)); }

void solve()
{
    int n;
    cin >> n;
    string s;
    cin >> s;
    set<char> sett;
    for (int i = 0; i < s.size(); i++)
    {
        sett.insert(s[i]);
    }

    map<char, int> r_map;
    string r = "";
    int ind = 0;
    for (char c : sett)
    {
        r += c;
        r_map[c] = ind;
        ind++;
    }
    string res = "";
    for (int i = 0; i < s.length(); i++)
    {
        res += r[r.length() - r_map[s[i]] - 1];
    }
    cout << res <<endl;
}

