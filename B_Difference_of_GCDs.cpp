const double EPS = 0.00000001;
const ll MOD = 1e9 + 7;
int gcd(ll a, ll b) { return b ? gcd(b, a % b) : a; }
int lcm(int a, int b) { return a * (b / gcd(a, b)); }
void solve()
{
    ll n, l, r;
    cin >> n >> l >> r;
    vector<ll> res;
    for (int i = 1; i <= n; i++)
    {
        ll m = (((l - 1) / i) + 1) * i; // this give us the smallest integer that's a multiple of i and strictly bigger than l;
        if (m > r)
        {
            cout << "NO" << endl;
            return;
        }
        else
        {
            res.push_back(m);
        }
    }
    cout << "YES" << endl;
    for (int i = 0; i < res.size(); i++)
    {
        cout << res[i] << " ";
    }

    cout << endl;
}

signed main()
{
    FAST;
    ll tt = 1;
    // freopen("input.in", "r", stdin);
    cin >> tt;
    while (tt--)
        solve();
    return 0;
}