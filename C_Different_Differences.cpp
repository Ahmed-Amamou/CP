const double EPS = 0.00000001;
const ll MOD = 1e9 + 7;
int gcd(ll a, ll b) { return b ? gcd(b, a % b) : a; }
int lcm(int a, int b) { return a * (b / gcd(a, b)); }

void solve()
{
    ll k, n;
    cin >> k >> n;
    vector<ll> ans = {1};
    int diff = 1;
    for (int i = 2; i <= k; i++)
    {
        ll next = ans.back() + diff;
        ll remaining = k - i;
        ll poss = n - next;
        if(remaining <= poss){
            ans.push_back(next);
            diff++;
        } 
        else{
            ans.push_back(ans.back()+1);
        }
    }
    for (auto& i : ans)
    {
            cout << i << " ";
    }
    cout << endl;
    
    
}

