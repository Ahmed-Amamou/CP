void solve()
{
    int k, n;
    cin >> k >> n;
    set<pair<int, int>> pairs;
    vector<int> v(n);
    for (int i = 0; i < n; i++)
    {
        cin >> v[i];
    }
    for (int i = 0; i < n; i++)
    {
        for (int j = i + 1; j < n; j++)
        {
            pairs.insert({v[i], v[j]});
        }
    }
    for (int i = 0; i < k - 1; i++)
    {
        for (int i = 0; i < n; i++)
        {
            cin >> v[i];
        }
        for (int i = 0; i < n; i++)
        {
            for (int j = i + 1; j < n; j++)
            {
                if (pairs.find({v[j], v[i]}) != pairs.end())
                {
                    pairs.erase({v[j], v[i]});
                }
            }
        }
    }
    cout << pairs.size() << endl;
}

