void solve()
{
    int x, y;
    cin >> x >> y;
    ll step = 1;
    ll counter = 0;
    ll sign = 1;
    ll position = x;
    while (true)
    {
        for (position; position != x + sign * step; position += sign)
        {
            // debug(counter, position);
            if (position == y)
            {
                break;
            }
            counter++;
        }
        if (position == y)
            break;
        sign *= -1;
        step *= 2;
    }
    cout << counter << endl;
}

